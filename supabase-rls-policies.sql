create table if not exists payment_events (
  razorpay_payment_id text primary key,
  razorpay_order_id text not null unique,
  user_id uuid not null references auth.users(id) on delete restrict,
  email text not null,
  plan text not null,
  amount integer not null check (amount >= 0),
  credits integer not null check (credits > 0),
  created_at timestamptz not null default now()
);

create table if not exists trial_claims (
  user_id uuid primary key references auth.users(id) on delete cascade,
  email text not null,
  claimed_at timestamptz not null default now()
);

create table if not exists credit_reservations (
  request_id uuid primary key,
  user_id uuid not null references auth.users(id) on delete cascade,
  email text not null,
  tool text not null,
  status text not null check (status in ('reserved', 'completed', 'refunded')),
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists api_rate_limits (
  subject_key text not null,
  route text not null,
  window_start timestamptz not null default now(),
  request_count integer not null default 0,
  primary key (subject_key, route)
);

-- Seed idempotency records for payments and trials handled before this upgrade.
insert into payment_events (
  razorpay_payment_id, razorpay_order_id, user_id, email, plan, amount, credits, created_at
)
select distinct on (razorpay_payment_id)
  razorpay_payment_id,
  razorpay_order_id,
  user_id,
  email,
  plan,
  greatest(0, coalesce(amount, 0)::integer),
  greatest(1, coalesce(credits, 1)),
  created_at
from orders
where status = 'paid'
  and user_id is not null
  and email is not null
  and plan is not null
  and razorpay_payment_id is not null
  and razorpay_order_id is not null
order by razorpay_payment_id, created_at asc
on conflict do nothing;

insert into trial_claims (user_id, email, claimed_at)
select distinct on (user_id) user_id, email, created_at
from orders
where status = 'trial' and user_id is not null and email is not null
order by user_id, created_at asc
on conflict (user_id) do nothing;

