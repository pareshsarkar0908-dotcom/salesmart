update credits set balance = 0 where balance < 0;

with ranked as (
  select
    id,
    user_id,
    row_number() over (partition by user_id order by updated_at desc, id desc) as row_number,
    sum(balance) over (partition by user_id) as merged_balance
  from credits
  where user_id is not null
),
keepers as (
  update credits c
  set balance = r.merged_balance, updated_at = now()
  from ranked r
  where c.id = r.id and r.row_number = 1
  returning c.user_id, c.id
)
delete from credits c
using ranked r
where c.id = r.id and r.row_number > 1;

create unique index if not exists credits_user_id_key on credits(user_id);
create index if not exists orders_user_created_idx on orders(user_id, created_at desc);
