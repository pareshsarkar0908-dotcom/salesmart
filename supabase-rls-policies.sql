create or replace function reserve_credit(
  p_request_id uuid,
  p_user_id uuid,
  p_email text,
  p_tool text
)
returns table(success boolean, balance integer, message text)
language plpgsql
security definer
set search_path = public
as $$
declare
  v_balance integer;
begin
  update credits
  set user_id = p_user_id, email = lower(p_email), updated_at = now()
  where user_id is null and lower(email) = lower(p_email);

  insert into credit_reservations(request_id, user_id, email, tool, status)
  values (p_request_id, p_user_id, lower(p_email), p_tool, 'reserved')
  on conflict (request_id) do nothing;

  if not found then
    select c.balance into v_balance from credits c where c.user_id = p_user_id;
    return query select false, coalesce(v_balance, 0), 'Duplicate generation request';
    return;
  end if;

  update credits
  set balance = credits.balance - 1, updated_at = now()
  where user_id = p_user_id and credits.balance > 0
  returning credits.balance into v_balance;

  if v_balance is null then
    delete from credit_reservations where request_id = p_request_id;
    select c.balance into v_balance from credits c where c.user_id = p_user_id;
    return query select false, coalesce(v_balance, 0), 'No credits remaining';
    return;
  end if;

  return query select true, v_balance, 'Credit reserved';
end;
$$;

create or replace function complete_credit_use(p_request_id uuid)
returns boolean
language plpgsql
security definer
set search_path = public
as $$
declare
  v_reservation credit_reservations%rowtype;
  v_balance integer;
begin
  update credit_reservations
  set status = 'completed', updated_at = now()
  where request_id = p_request_id and status = 'reserved'
  returning * into v_reservation;

  if v_reservation.request_id is null then
    return false;
  end if;

  select balance into v_balance from credits where user_id = v_reservation.user_id;

  insert into usage_logs(request_id, user_id, email, tool, cost, balance_after)
  values (
    p_request_id, v_reservation.user_id, v_reservation.email,
    v_reservation.tool, 1, coalesce(v_balance, 0)
  );
  return true;
end;
$$;

create or replace function refund_credit(p_request_id uuid)
returns table(refunded boolean, balance integer)
language plpgsql
security definer
set search_path = public
as $$
declare
  v_user_id uuid;
  v_balance integer;
begin
  update credit_reservations
  set status = 'refunded', updated_at = now()
  where request_id = p_request_id and status = 'reserved'
  returning user_id into v_user_id;

  if v_user_id is null then
    select c.balance into v_balance
    from credits c
    join credit_reservations r on r.user_id = c.user_id
    where r.request_id = p_request_id;
    return query select false, coalesce(v_balance, 0);
    return;
  end if;

  update credits
  set balance = credits.balance + 1, updated_at = now()
  where user_id = v_user_id
  returning credits.balance into v_balance;

  return query select true, coalesce(v_balance, 0);
end;
$$;

create or replace function grant_trial_atomic(
  p_user_id uuid,
  p_email text,
  p_credits integer default 10
)
returns table(granted boolean, balance integer)
language plpgsql
security definer
set search_path = public
as $$
declare
  v_claimed uuid;
  v_balance integer;
begin
  update credits
  set user_id = p_user_id, email = lower(p_email), updated_at = now()
  where user_id is null and lower(email) = lower(p_email);

  insert into trial_claims(user_id, email)
  values (p_user_id, lower(p_email))
  on conflict (user_id) do nothing
  returning user_id into v_claimed;

  if v_claimed is null then
    select c.balance into v_balance from credits c where c.user_id = p_user_id;
    return query select false, coalesce(v_balance, 0);
    return;
  end if;

  insert into credits(user_id, email, balance, updated_at)
  values (p_user_id, lower(p_email), p_credits, now())
  on conflict (user_id) do update
  set balance = credits.balance + excluded.balance,
      email = excluded.email,
      updated_at = now()
  returning credits.balance into v_balance;

  insert into orders(user_id, email, plan, amount, credits, status)
  values (p_user_id, lower(p_email), 'trial', 0, p_credits, 'trial');

  return query select true, v_balance;
end;
$$;

create or replace function finalize_payment_atomic(
  p_user_id uuid,
  p_email text,
  p_plan text,
  p_amount integer,
  p_credits integer,
  p_payment_id text,
  p_order_id text
)
returns table(processed boolean, balance integer)
language plpgsql
security definer
set search_path = public
as $$
declare
  v_inserted text;
  v_balance integer;
begin
  update credits
  set user_id = p_user_id, email = lower(p_email), updated_at = now()
  where user_id is null and lower(email) = lower(p_email);

  insert into payment_events(
    razorpay_payment_id, razorpay_order_id, user_id, email, plan, amount, credits
  )
  values (
    p_payment_id, p_order_id, p_user_id, lower(p_email), p_plan, p_amount, p_credits
  )
  on conflict do nothing
  returning razorpay_payment_id into v_inserted;

  if v_inserted is null then
    select c.balance into v_balance from credits c where c.user_id = p_user_id;
    return query select false, coalesce(v_balance, 0);
    return;
  end if;

  insert into credits(user_id, email, balance, updated_at)
  values (p_user_id, lower(p_email), p_credits, now())
  on conflict (user_id) do update
  set balance = credits.balance + excluded.balance,
      email = excluded.email,
      updated_at = now()
  returning credits.balance into v_balance;

  insert into orders(
    user_id, email, plan, amount, credits, status,
    razorpay_payment_id, razorpay_order_id
  )
  values (
    p_user_id, lower(p_email), p_plan, p_amount, p_credits, 'paid',
    p_payment_id, p_order_id
  );

  return query select true, v_balance;
end;
$$;

create or replace function check_rate_limit(
  p_subject_key text,
  p_route text,
  p_limit integer,
  p_window_seconds integer
)
returns table(allowed boolean, retry_after integer)
language plpgsql
security definer
set search_path = public
as $$
declare
  v_count integer;
  v_window_start timestamptz;
  v_now timestamptz := now();
begin
  insert into api_rate_limits(subject_key, route, window_start, request_count)
  values (p_subject_key, p_route, v_now, 1)
  on conflict (subject_key, route) do update
  set window_start = case
        when api_rate_limits.window_start <= v_now - make_interval(secs => p_window_seconds)
        then v_now else api_rate_limits.window_start end,
      request_count = case
        when api_rate_limits.window_start <= v_now - make_interval(secs => p_window_seconds)
        then 1 else api_rate_limits.request_count + 1 end
  returning request_count, window_start into v_count, v_window_start;

  return query
  select
    v_count <= greatest(1, p_limit),
    case when v_count <= greatest(1, p_limit) then 0
      else greatest(1, ceil(extract(epoch from (
        v_window_start + make_interval(secs => p_window_seconds) - v_now
      )))::integer)
    end;
end;
$$;

create or replace function admin_dashboard_summary()
returns jsonb
language sql
security definer
set search_path = public
as $$
  select jsonb_build_object(
    'totals', jsonb_build_object(
      'users', (select count(*) from credits),
      'credits', (select coalesce(sum(balance), 0) from credits),
      'orders', (select count(*) from orders),
      'revenue', (select coalesce(sum(amount), 0) from orders where status = 'paid')
    ),
    'orders', coalesce((
      select jsonb_agg(to_jsonb(recent_orders) order by created_at desc)
      from (
        select email, plan, amount, credits, status, created_at
        from orders
        order by created_at desc
        limit 50
      ) recent_orders
    ), '[]'::jsonb)
  );
$$;

